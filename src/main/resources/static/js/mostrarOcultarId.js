/**
 * This function show the ID
 */
function mostrarId()
{
    div = document.getElementById('flotante');
    div.style.display = '';
}
/**
 * This function hide the ID
 */
function ocultarId()
{
    div = document.getElementById('flotante');
    div.style.display = 'none';
}